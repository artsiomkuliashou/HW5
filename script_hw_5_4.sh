#!/bin/bash

string="$1"
num1="$2"
num2="$3"

# Проверка количества аргументов
if [ $# -ne 3 ]; then
    echo "Ошибка: требуется 3 аргумента" >&2
    echo "Использование: $0 строка число1 число2" >&2
    exit 1
fi

# Проверка что аргументы 2 и 3 - целые числа
if ! [[ "$num1" =~ ^[0-9]+$ ]]; then
    echo "Ошибка: аргумент 2 должен быть целым числом" >&2
    exit 1
fi

if ! [[ "$num2" =~ ^[0-9]+$ ]]; then
    echo "Ошибка: аргумент 3 должен быть целым числом" >&2
    exit 1
fi

# Проверка что числа не равны друг другу
if [ "$num1" -eq "$num2" ]; then
    echo "Ошибка: аргументы 2 и 3 не должны быть равны друг другу" >&2
    exit 1
fi

# Находим наибольшее число
if [ "$num1" -gt "$num2" ]; then
    max_num="$num1"
else
    max_num="$num2"
fi

# Проверка длины строки
string_length=${#string}
if [ "$string_length" -lt "$max_num" ]; then
    echo "Ошибка: длина строки ($string_length) должна быть больше или равна максимальному числу ($max_num)" >&2
    exit 1
fi

if [ "$num2" -gt "$num1" ]; then
    echo "Аргумент 3 ($num2) больше аргумента 2 ($num1)"
    # Здесь код для случая когда $3 > $2
    echo "$string" | cut -c $num1-$num2
else 
    echo "Аргумент 2 ($num1) больше аргумента 3 ($num2)"
    # Здесь код для случая когда $2 > $3
    echo "$string" | cut -c $num2-$num1
fi